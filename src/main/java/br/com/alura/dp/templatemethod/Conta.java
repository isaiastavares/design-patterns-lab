package br.com.alura.dp.templatemethod;

public class Conta {

	private String nome;
	private String agencia;
	private Integer numero;
	private Double saldo;

	public String getNome() {
		return nome;
	}

	public String getAgencia() {
		return agencia;
	}

	public Integer getNumero() {
		return numero;
	}

	public Double getSaldo() {
		return saldo;
	}

}
